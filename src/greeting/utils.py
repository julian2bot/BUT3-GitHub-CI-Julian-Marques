""" Utils.py les fonctions utiles"""


def add(a: int, b: int) -> int:
    """
    addition de deux nombre
    Args:
        a (int): nombre a
        b (int): nombre b
    return:
        Value(int): resultat de l'addition
    """
    return a + b


def sub(a: int, b: int) -> int:
    """
    soustrait de deux nombre
    Args:
        a (int): nombre a
        b (int): nombre b
    return:
        Value(int): resultat de la soustraction
    """
    return a - b
